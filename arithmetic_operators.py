a = 15
b = 4

#Addition +
print("Addition:", a+b)

#Subtraction
print("Subtraction:", a-b)

#Multiplication  *
print("Multiplication:", a*b)

#True Division (float result)
print("Float Division:", a/b)

#Floor Division(integer floor) //
print("Floor Division:", a//b)

#Modulus (reminder) %
print("Modulus:", a%b)

#Exponentiation (power)
print("Exponentiation:", a**b)