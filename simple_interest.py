#To calculate simple interest
p = 1000000 #principal amount
print("principal amount is:",p)
r = 8.5 #rate of interest
print("rate of interest is:",r)
t = 6 #time period in years
print("time period in years:",t)
simpleinterest = p*r*t / 100
print("SI:p*t*r/100")
print("simple ineterest is:",simpleinterest)
total_amount = simpleinterest + p #to calculate total amount
print("total amount after",t,"years",total_amount)
